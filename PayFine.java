    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        
        LocalDate today = LocalDate.now();
        
        try{
            
            MongoClient mongo = new MongoClient("localhost" , 27017);
            DB db = mongo.getDB("Library_System");
            DBCollection col = db.getCollection("paidfine");
            
            BasicDBObject b = new BasicDBObject("StudentID",studentid.getText()).append("paidAmount", payment.getText())
                    .append("Balance", balance.getText()).append("Date", today.toString());
            
            col.insert(b);
            
            JOptionPane.showMessageDialog(null, "Data Saved Successfully!");
            
            dispose();
            
        
        } catch (UnknownHostException ex) {
            Logger.getLogger(PayFine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveActionPerformed

