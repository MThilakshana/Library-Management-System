    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        
        if(studentid.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter Student ID");
        }
        else{
            float totalfineam = (float) 0.0 , totalpayment = (float) 0.0;
        
            try{
                MongoClient mongo = new MongoClient("localhost" , 27017);
                DB db = mongo.getDB("Library_System");
                DBCollection col = db.getCollection("bookfine");
                DBCollection col2 = db.getCollection("paidfine");
                
                BasicDBObject obj = new BasicDBObject();
                obj.put("StudentID" , studentid.getText());
                DBCursor cu = (DBCursor) col.find(obj).iterator();
                
                while(cu.hasNext()){
                    String amount = (String) cu.next().get("fine");
                    float am = Float.parseFloat(amount);
                    totalfineam = totalfineam + am;
                }
                
                DBCursor cu2 = (DBCursor) col2.find(obj).iterator();
                
                while(cu2.hasNext()){
                    String amount = (String) cu2.next().get("paidAmount");
                    float am = Float.parseFloat(amount);
                    totalpayment = totalpayment + am;
                }
                
                totalfine.setText(Float.toString(totalfineam - totalpayment));
                

        
            } catch (UnknownHostException ex) {
                Logger.getLogger(PayFine.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_searchActionPerformed

